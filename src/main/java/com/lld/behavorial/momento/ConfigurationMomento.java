package com.lld.behavorial.momento;

public class ConfigurationMomento {
    int height;
    int width;

    public ConfigurationMomento(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
